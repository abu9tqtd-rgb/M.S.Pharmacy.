// =====================
// CART SYSTEM
// =====================

let cart = [];
let total = 0;

function addToCart(name, price){

cart.push({
name,
price
});

total += price;

document.getElementById("cart-count")
.innerText = cart.length;

document.getElementById("total")
.innerText = total;

updateCart();

}

function updateCart(){

const cartItems =
document.getElementById("cart-items");

cartItems.innerHTML = "";

cart.forEach(item=>{

cartItems.innerHTML += `
<div class="cart-item">
<span>${item.name}</span>
<span>₹${item.price}</span>
</div>
`;

});

}

